/// Resolve `relative` against `base`, creating the directory so that
/// `canonicalize` succeeds. Use only for directories Tor does NOT own.
fn absolute_create(base: &Path, relative: &str) -> Result<PathBuf> {
    let joined = base.join(relative);
    std::fs::create_dir_all(&joined)?;
    Ok(joined.canonicalize()?)
}

/// Resolve `relative` against `base` WITHOUT creating the directory.
///
/// Used for `HiddenServiceDir`: Tor must create this directory itself
/// with mode 700.  Pre-creating it causes Tor to abort with a permissions
/// error.
fn absolute_no_create(base: &Path, relative: &str) -> PathBuf {
    // `base` is already absolute (derived from `current_exe()`), so joining
    // is sufficient. We skip canonicalize because the path may not exist yet.
    base.join(relative)
}
